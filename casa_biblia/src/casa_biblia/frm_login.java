    private void txt_userKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_userKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (txt_user.getText().length() == 8) {
                try {
                    Statement st = con.conexion();
                    usu.setNick(txt_user.getText().trim());
                    String buscar_nick = "select estado, contrasena from empleados where dni = '" + usu.getNick() + "'";
                    ResultSet rs = con.consulta(st, buscar_nick);
                    if (rs.next()) {
                        if (rs.getString("estado").equals("1")) {
                            usu.setClave(rs.getString("contrasena").trim());
                            txt_pass.setEnabled(true);
                            txt_pass.requestFocus();
                        } else {
                            JOptionPane.showMessageDialog(null, "USUARIO BLOQUEADO");
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "El usuario no existe.");
                        txt_user.setText("");
                        txt_user.requestFocus();
                    }
                } catch (SQLException ex) {
                    System.out.print(ex);
                    JOptionPane.showMessageDialog(null, ex.getLocalizedMessage());
                }
            } else {
                JOptionPane.showMessageDialog(null, "El numero de DNI esta incompleto");
            }
        }
    }//GEN-LAST:event_txt_userKeyPressed


    private void txt_passKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_passKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            String contrasena = txt_pass.getText().trim();
            if (contrasena.equals(usu.getClave())) {
                btn_ent.setEnabled(true);
                btn_ent.requestFocus();
            } else {
                JOptionPane.showMessageDialog(null, "La contraseña es incorrecta, \nIntente de nuevo");
                txt_pass.setText("");
                txt_pass.requestFocus();
            }
        }
    }//GEN-LAST:event_txt_passKeyPressed

