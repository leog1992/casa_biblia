    private void saludar_usuario(String dni) {
        try {
            Statement st = con.conexion();
            String ver_datos = "select nombres, ape_pat, ape_mat from empleados where dni = '" + dni + "'";
            ResultSet rs = con.consulta(st, ver_datos);
            if (rs.next()) {
                Notification.show("Ingreso exitoso a lunaPOS", "Bienvenido Estimado(a) " + rs.getString("nombres") + " " + rs.getString("ape_pat") + " " + rs.getString("ape_mat"), Notification.NICON_LIGHT_THEME);
            }
        } catch (SQLException e) {
            System.out.println(e.getLocalizedMessage());
        }
    }

