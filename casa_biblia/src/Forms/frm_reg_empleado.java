    private void llenar() {
        per.setEmpresa(cbx_empresa.getSelectedItem().toString().trim());
        per.setTienda(cbx_tienda.getSelectedIndex() + 1);
        per.setNro_doc(txt_ndoc.getText());
        per.setNom_per(txt_nom.getText().toUpperCase());
        per.setApe_pat(txt_ape_pat.getText().toUpperCase());
        per.setApe_mat(txt_ape_mat.getText().toUpperCase());
        per.setDir_per(txt_dir.getText().toUpperCase());
        per.setTel_per(Integer.parseInt(txt_tel.getText()));
        per.setEmail(txt_email.getText().toLowerCase());
        per.setContrasena(txt_password.getText().toLowerCase().trim());
        per.setDiario(Double.parseDouble(txt_jor.getText()));
        per.setCargo(txt_cargo.getText().toUpperCase().trim());
        per.setFecha_nacimiento(ven.fechabase(txt_fecha.getText()));
        per.setEst_per("1");
    }
    private void btn_regActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_regActionPerformed
        llenar();
        if (accion.equals("registrar")) {
            try {
                Statement st = con.conexion();
                String insert_per = "insert into empleados Values ('" + per.getNro_doc() + "', '" + per.getEmpresa() + "', '" + per.getTienda() + "', '" + per.getNom_per() + "', '" + per.getApe_pat() + "', '" + per.getApe_mat() + "', '" + per.getCargo()+ "',  '" + per.getDir_per() + "', '" + per.getTel_per() + "', "
                        + "'" + per.getFecha_nacimiento()+ "', '" + per.getDiario() + "', '" + per.getEmail() + "', '" + per.getContrasena() + "', '" + per.getEst_per() + "')";
                con.actualiza(st, insert_per);
                con.cerrar(st);
            } catch (Exception e) {
                System.out.println(e.getLocalizedMessage());
            }
            JOptionPane.showMessageDialog(null, "Se ha ingresado los datos");
        }
        if (accion.equals("modificar")) {
            try {
                Statement st = con.conexion();
                String insert_per = "update empleados set nombres = '" + per.getNom_per() + "', ape_pat = '" + per.getApe_pat() + "', ape_mat = '" + per.getApe_mat() + "', direccion = '" + per.getDir_per() + "', telefono = '" + per.getTel_per() + "', "
                        + "diario = '" + per.getDiario() + "' where dni = '" + per.getNro_doc() + "'";
                con.actualiza(st, insert_per);
                con.cerrar(st);
            } catch (Exception e) {
                System.out.println(e.getLocalizedMessage());
            }
            JOptionPane.showMessageDialog(null, "Se ha modificado los datos");
        }
        frm_ver_empleados empleado = new frm_ver_empleados();
        ven.llamar_ventana(empleado);
        accion = "";
        this.dispose();
    }//GEN-LAST:event_btn_regActionPerformed

