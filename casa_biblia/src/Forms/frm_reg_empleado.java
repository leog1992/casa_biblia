    private void txt_ndocKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_ndocKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            try {
                per.setNro_doc(txt_ndoc.getText());
                Statement st = con.conexion();
                String buscar = "select * from empleados where dni = '" + per.getNro_doc() + "'";
                ResultSet rs = con.consulta(st, buscar);
                if (rs.next()) {
                    JOptionPane.showMessageDialog(null, "El empleado ya existe, ingrese otro dni");
                    txt_ndoc.setText("");
                    txt_ndoc.requestFocus();
                } else {
                    txt_nom.setEnabled(true);
                    txt_nom.requestFocus();
                }
            } catch (SQLException ex) {
                System.out.print(ex);
            }
        }
    }//GEN-LAST:event_txt_ndocKeyPressed

