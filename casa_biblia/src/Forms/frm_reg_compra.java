    private boolean comprobar_doc(String ruc, String ser, int nro, int idtido) {
        boolean existe = false;
        try {
            Statement st = con.conexion();
            String ver_doc = "select id_compra, periodo from compras where ruc_proveedor = '" + ruc + "' and serie = '" + ser + "' and numero = '" + nro + "' and id_tido = '" + idtido + "'";
            System.out.println(ver_doc);
            ResultSet rs = con.consulta(st, ver_doc);
            if (rs.next()) {
                existe = true;
                System.out.println(rs.getInt("id_compra") + "-" + rs.getString("periodo"));
            }
        } catch (Exception e) {
            System.out.println(e.getLocalizedMessage());
        }
        return existe;
    }

    private void txt_rucKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_rucKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (txt_ruc.getText().length() == 11) {
                pro.setRuc(txt_ruc.getText());
                boolean ruc_valido = pro.validar_RUC(pro.getRuc());
                if (ruc_valido == true) {
                    try {
                        Statement st = con.conexion();
                        String ver_pro = "select razon_social from proveedores where ruc_proveedor = '" + pro.getRuc() + "'";
                        ResultSet rs = con.consulta(st, ver_pro);
                        if (rs.next()) {
                            boolean comprueba_doc = comprobar_doc(pro.getRuc(), txt_ser.getText(), Integer.parseInt(txt_ndoc.getText()), cbx_tido.getSelectedIndex() + 1);
                            if (comprueba_doc == false) {
                                pro.setRaz_soc(rs.getString("razon_social"));
                                txt_raz.setText(rs.getString("razon_social"));
                                cbx_mon.setEnabled(true);
                                cbx_mon.requestFocus();
                            } else {
                                txt_ruc.setText("");
                                txt_ruc.requestFocus();
                                JOptionPane.showMessageDialog(null, "El Documento ya existe");
                            }
                        } else {
                            JOptionPane.showMessageDialog(null, "El Proveedor no existe");
                            frm_reg_proveedor proveedor = new frm_reg_proveedor();
                            frm_reg_proveedor.origen = "compra";
                            frm_reg_proveedor.accion = "registrar";
                            ven.llamar_ventana(proveedor);
                        }
                    } catch (SQLException ex) {
                        System.out.print(ex);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "EL RUC INGRESADO NO ES VALIDO \nVERIFIQUE POR FAVOR");
                }
            } else {
                JOptionPane.showMessageDialog(null, "FALTAN DIGITOS AL NUMERO DE RUC");
            }
        }
    }//GEN-LAST:event_txt_rucKeyPressed

