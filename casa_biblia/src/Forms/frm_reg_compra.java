                String mes_fec = fec_com.charAt(3) + "" + fec_com.charAt(4);
                String anio_fec = fec_com.charAt(6) + "" + fec_com.charAt(7) + "" + fec_com.charAt(8) + "" + fec_com.charAt(9);
                //separar mes y a√±o de periodo
                String per_txt = txt_per.getText();
                String mes_per = per_txt.charAt(0) + "" + per_txt.charAt(1);
                String anio_per = per_txt.charAt(3) + "" + per_txt.charAt(4) + "" + per_txt.charAt(5) + "" + per_txt.charAt(6);
                //comparar fecha y periodo
                if (Integer.parseInt(anio_fec) <= Integer.parseInt(anio_per)) {
                    txt_ruc.setEnabled(true);
                    txt_ruc.requestFocus();
                    txt_id.setText("" + ver_cod(per_txt, empresa));
                } else if (Double.parseDouble(mes_fec) <= Double.parseDouble(mes_per)) {
                    txt_ruc.setEnabled(true);
                    txt_ruc.requestFocus();
                    txt_id.setText("" + ver_cod(per_txt, empresa));
                } else {
                    JOptionPane.showMessageDialog(null, "Fecha no valida en el periodo");
                }

