    private Integer ver_cod(String periodo, String empresa) {
        Integer cod_com = 0;
        try {
            Statement st = con.conexion();
            String ver_cod_per = "select id_compra from compras where periodo = '" + periodo + "' and empresa = '" + empresa + "' order by id_compra desc limit 1";
            ResultSet rs = con.consulta(st, ver_cod_per);
            if (rs.next()) {
                cod_com = rs.getInt("id_compra");
            } else {
                cod_com = 0;
            }
            con.cerrar(rs);
            con.cerrar(st);
        } catch (SQLException e) {
            System.out.println(e.getLocalizedMessage());
        }
        cod_com = cod_com + 1;
        return cod_com;
    }

    private void cargar_proveedores() {
        try {
            // autocompletar = new TextAutoCompleter(txt_buscar_producto);
            TextAutoCompleter autocompletar = new TextAutoCompleter(txt_ruc, new AutoCompleterCallback() {
                @Override
                public void callback(Object selectedItem) {
                    txt_ruc.setText("");
                    txt_ruc.requestFocus();
                    String cadena = selectedItem.toString();
                    JOptionPane.showMessageDialog(null, "El usuario seleccion√≥: " + cadena);
                }
            });

            autocompletar.setMode(0);
            autocompletar.setCaseSensitive(false);
            Statement st = con.conexion();
            String sql = "select ruc_proveedor from proveedores order by ruc_proveedor asc";
            ResultSet rs = con.consulta(st, sql);
            while (rs.next()) {
                autocompletar.addItem(rs.getString("ruc_proveedor"));
            }
            con.cerrar(rs);
            con.cerrar(st);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error " + e.getLocalizedMessage());
            System.out.println(e.getLocalizedMessage());
        }
    }

