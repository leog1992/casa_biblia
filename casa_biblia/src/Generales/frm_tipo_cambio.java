    private void llenar() {
        fecha = ven.fechabase(txt_fec.getText());
        moneda = cbx_moneda.getSelectedIndex() + 1;
        pvta = Double.parseDouble(txt_ven.getText());
        pcom = Double.parseDouble(txt_com.getText());
    }

    private void btn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addActionPerformed
        llenar();
        if (accion.equals("registrar")) {
            try {
                Statement st = con.conexion();
                String ins_cam = "insert into tipo_cambio Values ('" + fecha + "', '" + pcom + "', '" + pvta + "', '" + moneda + "')";
                System.out.println(ins_cam);
                con.actualiza(st, ins_cam);
                con.cerrar(st);
            } catch (Exception ex) {
                System.out.print(ex);
                JOptionPane.showMessageDialog(null, ex.getLocalizedMessage());
            }
        }

        if (accion.equals("modificar")) {
            try {
                Statement st = con.conexion();
                String upd_cam = "update tipo_cambio set compra = '" + pcom + "', venta = '" + pvta + "' where fecha = '" + fecha + "' and "
                        + "id_moneda = '" + moneda + "'";
                System.out.println(upd_cam);
                con.actualiza(st, upd_cam);
                con.cerrar(st);
            } catch (Exception ex) {
                System.out.print(ex);
                JOptionPane.showMessageDialog(null, ex.getLocalizedMessage());
            }
        }

        ver_cambios();
        accion = null;
        txt_fec.setEnabled(true);
        cbx_moneda.setEnabled(false);
        txt_ven.setEnabled(false);
        txt_com.setEnabled(false);
        txt_ven.setText("");
        txt_com.setText("");
        btn_cer.setEnabled(true);
        txt_fec.requestFocus();
    }//GEN-LAST:event_btn_addActionPerformed

