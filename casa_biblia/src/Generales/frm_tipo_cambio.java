    private void ver_cambios() {
        mostrar = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int fila, int columna) {
                return false;
            }
        };
        mostrar.addColumn("Fecha");
        mostrar.addColumn("Moneda");
        mostrar.addColumn("P. Vta.");
        mostrar.addColumn("P. Com.");
        try {
            Statement st = con.conexion();
            String ver_cam = "select tc.fecha, m.nombre, tc.compra, tc.venta from tipo_cambio as tc inner join moneda as m on tc.id_moneda = "
                    + "m.id_moneda where month(tc.fecha) = month(current_date()) and  year(tc.fecha) = year(current_date()) order by tc.fecha desc";
            ResultSet rs = con.consulta(st, ver_cam);
            Object fila[] = new Object[4];
            while (rs.next()) {
                fila[0] = ven.fechaformateada(rs.getString("fecha"));
                fila[1] = rs.getString("nombre").toUpperCase();
                fila[2] = ven.formato_tc(rs.getDouble("venta"));
                fila[3] = ven.formato_tc(rs.getDouble("compra"));
                mostrar.addRow(fila);
            }
            con.cerrar(rs);
            con.cerrar(st);

        } catch (SQLException e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(null, e.getLocalizedMessage());
        }
        t_cambio.setModel(mostrar);
        t_cambio.getColumnModel().getColumn(0).setPreferredWidth(80);
        t_cambio.getColumnModel().getColumn(1).setPreferredWidth(120);
        t_cambio.getColumnModel().getColumn(2).setPreferredWidth(50);
        t_cambio.getColumnModel().getColumn(3).setPreferredWidth(50);
        ven.centrar_celda(t_cambio, 0);
        ven.derecha_celda(t_cambio, 2);
        ven.derecha_celda(t_cambio, 3);
    }

    private void ver_moneda() {
        ArrayList lista_moneda = new ArrayList();
        try {
            Statement st = con.conexion();
            String query = "select nombre from moneda order by id_moneda asc";
            ResultSet rs = con.consulta(st, query);
            while (rs.next()) {
                lista_moneda.add(rs.getString("nombre"));
            }
            con.cerrar(st);
            con.cerrar(rs);
        } catch (SQLException ex) {
            System.out.print(ex);
            JOptionPane.showMessageDialog(null, ex.getLocalizedMessage());
        }
        for (Object moneda_cbx : lista_moneda) {
            cbx_moneda.addItem(moneda_cbx);
        }
    }

