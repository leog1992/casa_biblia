    private void cbx_monedaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_cbx_monedaKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (!cbx_moneda.getSelectedItem().equals("SOLES")) {
                fecha = ven.fechabase(txt_fec.getText());
                moneda = cbx_moneda.getSelectedIndex() + 1;
                try {
                    Statement st = con.conexion();
                    String comprueba_fecha = "select compra, venta from tipo_cambio where fecha = '" + fecha + "' and id_moneda = '" + moneda + "'";
                    ResultSet rs = con.consulta(st, comprueba_fecha);
                    if (rs.next()) {
                        accion = "modificar";
                        txt_com.setText(ven.formato_tc(rs.getDouble("compra")));
                        txt_com.setEnabled(true);
                        txt_ven.setText(ven.formato_tc(rs.getDouble("venta")));
                        txt_ven.setEnabled(true);
                        txt_com.requestFocus();
                    } else {
                        accion = "registrar";
                        txt_com.setEnabled(true);
                        txt_com.requestFocus();
                    }
                    con.cerrar(rs);
                    con.cerrar(st);
                } catch (Exception e) {
                    System.out.println(e);
                    JOptionPane.showMessageDialog(null, e.getLocalizedMessage());
                }
            } else {
                JOptionPane.showMessageDialog(null, "SELECCIONE OTRA MONEDA");
            }
        }
    }//GEN-LAST:event_cbx_monedaKeyPressed

