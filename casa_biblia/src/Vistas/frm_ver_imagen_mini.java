    public static void cargar_imagen(String imgname) {
        try {
            //cargar imagen 
            ImagenURL Imagen = new ImagenURL();
            //JOptionPane.showMessageDialog(null, "el nombre de imagen es " + imgname);
            //System.out.println(imgname);
            if (imgname == null || "".equals(imgname) || imgname.equals("null")) {
                imgname = "noimage.jpg";
            }
            String url;
            String server = "www.lunasystemsperu.com";
            url = "http://" + server + "/casa_biblia/imagenes/productos/" + imgname;
           // System.out.println(url + "\n");
            Imagen.setlink(url);
            jp_imagen.add(Imagen);
            jp_imagen.repaint();
            //fin de carga
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error al cargar imagen");
            System.out.println(ex);
        }
    }

    public static class ImagenURL extends javax.swing.JPanel {

        String link;

        public ImagenURL() {
            this.setSize(200, 200); //se selecciona el tama√±o del panel
        }

        public void paint(Graphics grafico) {
            Dimension height = getSize();
            URL url;
            ImageIcon Img = null;
            try {
                url = new URL(link);
                BufferedImage c = ImageIO.read(url);
                Img = new ImageIcon(c);
                grafico.drawImage(Img.getImage(), 0, 0, height.width, height.height, null);
                setOpaque(false);
                super.paintComponent(grafico);
            } catch (MalformedURLException ex) {
                System.out.print(ex);
                JOptionPane.showMessageDialog(null, "no se puede mostrar la imagen \nerror con " + ex);
            } catch (IOException ex) {
                System.out.print(ex);
                JOptionPane.showMessageDialog(null, "no se puede mostrar la imagen \nerror con " + ex);
            }
//            grafico.drawImage(Img.getImage(), 0, 0, height.width, height.height, null);
//            setOpaque(false);
//            super.paintComponent(grafico);
        }

        public void setlink(String url) {
            link = url;
        }

        public String getlink() {
            return link;
        }
    }

