    private void txt_busKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_busKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_F1) {
            txt_bus.setText("");
            txt_bus.requestFocus();
        }
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            String buscar = txt_bus.getText();
            String query = "select p.id_producto, al.nombre as tienda, p.descripcion, p.codigo_externo, f.descripcion as nombre_familia, cf.descripcion as nombre_clase, pe.ubicacion, p.tipo_producto, um.corto, pe.cantidad_actual, pe.cantidad_minima, p.precio "
                    + "from productos_almacenes as pe inner join productos as p on pe.id_producto = p.id_producto inner join unidad_medida as um on "
                    + "p.id_und_med = um.id_und_med inner join familia_productos as f on p.familia = f.id  inner join clasificacion_familia as cf "
                    + "on cf.id= p.clase_familia and cf.familia=p.familia inner join almacenes as al on pe.id_almacen = al.id_almacen and pe.empresa = al.empresa where p.codigo_externo like '%" + buscar + "%' or p.descripcion like '%" + buscar + "%' order by p.descripcion asc, pe.empresa asc, pe.id_almacen asc";
            pro.ver_productos_tiendas(t_productos, query);
            contar_filas();
        }
    }//GEN-LAST:event_txt_busKeyPressed

