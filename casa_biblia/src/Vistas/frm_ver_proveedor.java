    private void btn_modActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_modActionPerformed
        frm_reg_proveedor prov = new frm_reg_proveedor();
        try {
            String idpro = t_proveedor.getValueAt(i, 0).toString();
            String query = "select * from proveedores where ruc_proeedor = '" + idpro + "'";
            Statement st = con.conexion();
            ResultSet rs = con.consulta(st, query);
            if (rs.next()) {
                prov.txt_ruc.setText(rs.getString("ruc_proeedor"));
                prov.txt_ruc.setEditable(false);
                prov.txt_raz.setText(rs.getString("razon_social"));
                prov.txt_raz.setEditable(true);
                prov.txt_raz.requestFocus();
                prov.txt_dir.setText(rs.getString("direccion"));
                prov.txt_dir.setEditable(true);
                prov.txt_web.setText(rs.getString("web"));
                prov.txt_web.setEditable(true);
                prov.accion = "modificar";
                prov.btn_reg.setEnabled(true);
                ven.llamar_ventana(prov);
                this.dispose();
            }
        } catch (SQLException ex) {
            System.out.print(ex);
            JOptionPane.showMessageDialog(null, ex.getLocalizedMessage());
        }
    }//GEN-LAST:event_btn_modActionPerformed

    private void btn_eliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliActionPerformed
        int confirmado = JOptionPane.showConfirmDialog(
                null,
                "Â¿Confirma eliminar el Proveedor?");

        if (JOptionPane.OK_OPTION == confirmado) {
            try {
                Statement st = con.conexion();
                String eli_cli = "delete from proveedores where ruc_proveedor = '" + pro.getRuc() + "'";
                con.actualiza(st, eli_cli);
                con.cerrar(st);
                String query = "select ruc_proveedor, razon_social, direccion from proveedores order by razon_social asc";
                ver_proveedor(query);
            } catch (Exception ex) {
                System.out.print(ex.getLocalizedMessage());
                JOptionPane.showMessageDialog(null, "Error al Eliminar!");
                JOptionPane.showMessageDialog(null, ex.getLocalizedMessage());
            }
        } else {
            System.out.print("no hago nada!");
        }
        btn_eli.setEnabled(false);
    }//GEN-LAST:event_btn_eliActionPerformed

