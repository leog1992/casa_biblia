    private void ver_proveedor(String query) {
        try {
            mostrar = new DefaultTableModel() {
                @Override
                public boolean isCellEditable(int fila, int columna) {
                    return false;
                }
            };
            Statement st = con.conexion();
            ResultSet rs = con.consulta(st, query);

            mostrar.addColumn("RUC");
            mostrar.addColumn("Razon Social");
            mostrar.addColumn("Direccion");

            //Creando las filas para el JTable
            while (rs.next()) {
                Object[] fila = new Object[3];
                fila[0] = rs.getString("ruc_proveedor");
                fila[1] = rs.getString("razon_social").toUpperCase();
                fila[2] = rs.getString("direccion");
                mostrar.addRow(fila);
            }
            con.cerrar(st);
            con.cerrar(rs);
            t_proveedor.setModel(mostrar);
            t_proveedor.getColumnModel().getColumn(0).setPreferredWidth(40);
            t_proveedor.getColumnModel().getColumn(1).setPreferredWidth(300);
            t_proveedor.getColumnModel().getColumn(2).setPreferredWidth(150);
            ven.centrar_celda(t_proveedor, 0);
            mostrar.fireTableDataChanged();
        } catch (SQLException e) {
            System.out.print(e);
            JOptionPane.showMessageDialog(null, e.getLocalizedMessage());
        }

    }

