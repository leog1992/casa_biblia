    private void btn_fijarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_fijarActionPerformed
        if (i > -1) {
            try {
                String almacen = t_empresas.getValueAt(i, 0).toString();
                String ruta = "tienda.txt";
                File archivo = new File(ruta);
                BufferedWriter bw;
                if (archivo.exists()) {
                    bw = new BufferedWriter(new FileWriter(archivo));
                    bw.write(almacen);
                } else {
                    bw = new BufferedWriter(new FileWriter(archivo));
                    bw.write(almacen);
                }
                bw.close();
            } catch (IOException ex) {
                System.out.print(ex);
            }

            try {
                String empresa = t_empresas.getValueAt(i, 1).toString();
                String ruta = "empresa.txt";
                File archivo = new File(ruta);
                BufferedWriter bw;
                if (archivo.exists()) {
                    bw = new BufferedWriter(new FileWriter(archivo));
                    bw.write(empresa);
                } else {
                    bw = new BufferedWriter(new FileWriter(archivo));
                    bw.write(empresa);
                }
                bw.close();
            } catch (IOException ex) {
                System.out.print(ex);
            }
            JOptionPane.showMessageDialog(null, "Se ha establecido correctamente.\nReingrese al Sistema");
            System.exit(0);
        }
    }//GEN-LAST:event_btn_fijarActionPerformed

