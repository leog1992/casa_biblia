    private void ver_empresas(String query) {
        try {
            mostrar = new DefaultTableModel() {
                @Override
                public boolean isCellEditable(int fila, int columna) {
                    return false;
                }
            };
            Statement st = con.conexion();
            ResultSet rs = con.consulta(st, query);

            mostrar.addColumn("RUC");
            mostrar.addColumn("Razon Social");
            mostrar.addColumn("Direccion");

            //Creando las filas para el JTable
            while (rs.next()) {
                Object[] fila = new Object[3];
                fila[0] = rs.getString("ruc_empresa");
                fila[1] = rs.getString("razon_social");
                fila[2] = rs.getString("direccion").toUpperCase().trim();
                mostrar.addRow(fila);
            }
            con.cerrar(st);
            con.cerrar(rs);
            t_empresas.setModel(mostrar);
            t_empresas.getColumnModel().getColumn(0).setPreferredWidth(95);
            t_empresas.getColumnModel().getColumn(1).setPreferredWidth(450);
            t_empresas.getColumnModel().getColumn(2).setPreferredWidth(450);
            ven.centrar_celda(t_empresas, 0);
            mostrar.fireTableDataChanged();
        } catch (SQLException e) {
            System.out.print(e);
            JOptionPane.showMessageDialog(null, e.getLocalizedMessage());
        }
    }

