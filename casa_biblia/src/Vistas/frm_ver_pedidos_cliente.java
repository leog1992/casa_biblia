    private void modelo_pedidos(String query) {

        model_pedidos = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int fila, int columna) {
                return false;
            }
        };
        model_pedidos.addColumn("COD");
        model_pedidos.addColumn("AÃ‘O");
        model_pedidos.addColumn("FECHA PED.");
        model_pedidos.addColumn("FECHA COBRO");
        model_pedidos.addColumn("VENDEDOR");
        model_pedidos.addColumn("TOTAL");
        model_pedidos.addColumn("CAJERO");
        model_pedidos.addColumn("ESTADO");
        Object[] fila = new Object[8];
        try {
            Statement st = con.conexion();
            ResultSet rs = con.consulta(st, query);
            while (rs.next()) {
                fila[0] = rs.getInt("id");
                fila[1] = rs.getInt("anio");
                fila[2] = ven.fecha_larga_tabla(rs.getString("fecha_pedido"));
                String fecha_cobro = rs.getString("fecha_cobro");
                String fecha_larga = ven.fecha_larga_tabla(fecha_cobro);
                if (fecha_larga.equals("01/01/2070 00:00:01") || fecha_cobro.equals("2070-01-01 00:00:00")) {
                    fila[3] = "-";
                } else {
                    fila[3] = ven.fecha_larga_tabla(fecha_cobro);
                }
                fila[4] = rs.getString("ev.nombres").charAt(0) + rs.getString("ev.ape_pat");
                fila[5] = ven.formato_numero(rs.getDouble("total"));

                String estado = rs.getString("estado");
                if (estado.equals("0")) {
                    fila[7] = "PENDIENTE";
                    fila[6] = "-";
                } else {
                    fila[7] = "CANCELADO";
                    fila[6] = rs.getString("cajero");
                }
                model_pedidos.addRow(fila);
            }
            con.cerrar(rs);
            con.cerrar(st);
            t_pedidos.setModel(model_pedidos);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getLocalizedMessage());
        }

    }

